<template>
    <div :id="id" style="height: 100%; weight: 100%;" @mouseover="MouseOverChartCell($event)">
        <el-row justify="end" style="margin-left: 64.8%; margin-bottom: 1%;">
            <el-button info>
                <el-icon class="el-icon--left">
                    <Back />
                </el-icon>Reset Data or Chart Type
            </el-button>
            <el-button plain>
                Delete<el-icon class="el-icon--right">
                    <Delete />
                </el-icon>
            </el-button>
        </el-row>
        <div :id="svgId" class="vis-svg" @click="Hover($event)">
        </div>
    </div>
</template>
  
<script>
import * as d3 from "d3";
import axios from "axios";
import { render, ref } from 'vue';
import ChartCellToolbar from "@/components/ChartComponents/ChartCellToolbar.vue";

let vm = null;

export default {
    name: "StackedBarChart_Normalized",
    props: {
        index: null,
        sbIndex: null,
        dataSourceURL: null,
        ChartCellToolbar,
    },
    data() {
        return {
            id: "stacked-bar-chart-normalized-" + this.sbIndex + "-" + this.index,
            svgId: "svg-" + this.sbIndex + "-" + this.index,
            dataURL: this.dataSourceURL,
            data: null
        };
    },
    components: {},
    methods: {
        MouseOverChartCell(event) {

        },
        draw(data_, mySvgId) {
            let nameOfY = data_.columns[0]

            var ages = data_.columns.slice(1)
            var stateages = ages.flatMap(age => data_.map(d => ({ state: d[nameOfY], age, population: d[age] }))) // pivot longer

            let mySvg = document.getElementById(mySvgId)
            let myWidth = mySvg.offsetWidth;
            let myHeight = mySvg.offsetHeight;

            var chart = StackedBarChart(stateages, {
                x: d => d.population,
                y: d => d.state,
                z: d => d.age,
                yDomain: d3.groupSort(
                    stateages,
                    (D) => D[0].population / d3.sum(D, d => d.population), // proportion of first age group
                    d => d.state // sort y by x
                ),
                zDomain: ages,
                colors: d3.schemeBlues[ages.length],
                width: myWidth,
                height: myHeight
            })

            mySvg.appendChild(chart);

            // Copyright 2021 Observable, Inc.
            // Released under the ISC license.
            // https://observablehq.com/@d3/stacked-normalized-horizontal-bar
            function StackedBarChart(data, {
                x = d => d, // given d in data, returns the (quantitative) x-value
                y = (d, i) => i, // given d in data, returns the (ordinal) y-value
                z = () => true, // given d in data, returns the (categorical) z-value
                title, // given d in data, returns the title text
                marginTop = 30, // top margin, in pixels
                marginRight = 20, // right margin, in pixels
                marginBottom = 0, // bottom margin, in pixels
                marginLeft = 40, // left margin, in pixels
                width = 640, // outer width, in pixels
                height, // outer height, in pixels
                xType = d3.scaleLinear, // type of x-scale
                xDomain, // [xmin, xmax]
                xRange = [marginLeft, width - marginRight], // [left, right]
                yDomain, // array of y-values
                yRange, // [bottom, top]
                yPadding = 0.1, // amount of y-range to reserve to separate bars
                zDomain, // array of z-values
                offset = d3.stackOffsetExpand, // stack offset method
                order = d3.stackOrderNone, // stack order method
                xFormat = "%", // a format specifier string for the x-axis
                xLabel, // a label for the x-axis
                colors = d3.schemeTableau10, // array of colors
            } = {}) {
                // Compute values.
                const X = d3.map(data, x);
                const Y = d3.map(data, y);
                const Z = d3.map(data, z);

                // Compute default y- and z-domains, and unique them.
                if (yDomain === undefined) yDomain = Y;
                if (zDomain === undefined) zDomain = Z;
                yDomain = new d3.InternSet(yDomain);
                zDomain = new d3.InternSet(zDomain);

                // Omit any data not present in the y- and z-domains.
                const I = d3.range(X.length).filter(i => yDomain.has(Y[i]) && zDomain.has(Z[i]));

                // If the height is not specified, derive it from the y-domain.
                if (height === undefined) height = yDomain.size * 25 + marginTop + marginBottom;
                if (yRange === undefined) yRange = [height - marginBottom, marginTop];

                // Compute a nested array of series where each series is [[x1, x2], [x1, x2],
                // [x1, x2], â€¦] representing the x-extent of each stacked rect. In addition,
                // each tuple has an i (index) property so that we can refer back to the
                // original data point (data[i]). This code assumes that there is only one
                // data point for a given unique y- and z-value.
                const series = d3.stack()
                    .keys(zDomain)
                    .value(([, I], z) => X[I.get(z)])
                    .order(order)
                    .offset(offset)(d3.rollup(I, ([i]) => i, i => Y[i], i => Z[i]))
                    .map(s => s.map(d => Object.assign(d, { i: d.data[1].get(s.key) })));

                // Compute the default y-domain. Note: diverging stacks can be negative.
                if (xDomain === undefined) xDomain = d3.extent(series.flat(2));

                // Construct scales, axes, and formats.
                const xScale = xType(xDomain, xRange);
                const yScale = d3.scaleBand(yDomain, yRange).paddingInner(yPadding);
                const color = d3.scaleOrdinal(zDomain, colors);
                const xAxis = d3.axisTop(xScale).ticks(width / 80, xFormat);
                const yAxis = d3.axisLeft(yScale).tickSizeOuter(0);

                // Compute titles.
                if (title === undefined) {
                    title = i => `${Y[i]}\n${Z[i]}\n${X[i].toLocaleString()}`;
                } else {
                    const O = d3.map(data, d => d);
                    const T = title;
                    title = i => T(O[i], i, data);
                }

                const svg = d3.create("svg")
                    .attr("width", width)
                    .attr("height", height)
                    .attr("viewBox", [0, 0, width, height])
                    .attr("style", "max-width: 100%; height: auto; height: intrinsic;");

                const bar = svg.append("g")
                    .selectAll("g")
                    .data(series)
                    .join("g")
                    .attr("fill", ([{ i }]) => color(Z[i]))
                    .selectAll("rect")
                    .data(d => d)
                    .join("rect")
                    .attr("id", function (d, i) {
                        let index = i
                        return vm.id + "-rect-" + index
                    })
                    .attr("x", ([x1, x2]) => Math.min(xScale(x1), xScale(x2)))
                    .attr("y", ({ i }) => yScale(Y[i]))
                    .attr("width", ([x1, x2]) => Math.abs(xScale(x1) - xScale(x2)))
                    .attr("height", yScale.bandwidth())
                    .on("click", function () {
                        let fontColor = "#aabe9b"
                        d3.select(this).style("fill", "#cae6b6")
                            .attr("stroke", fontColor)
                            .attr("stroke-width", "5")
                    });

                let idd = "stacked-bar-chart-normalized-1-2-rect-1"

                if (title) bar.append("title")
                    .text(({ i }) => title(i));

                svg.append("g")
                    .attr("transform", `translate(0,${marginTop})`)
                    .call(xAxis)
                    .call(g => g.select(".domain").remove())
                    .call(g => g.append("text")
                        .attr("x", width - marginRight)
                        .attr("y", -22)
                        .attr("fill", "currentColor")
                        .attr("text-anchor", "end")
                        .text(xLabel));

                svg.append("g")
                    .attr("transform", `translate(${xScale(0)},0)`)
                    .call(yAxis);

                return Object.assign(svg.node(), { scales: { color } });
            }
        },
    },
    mounted() {
        vm = this;

        const path = vm.dataURL;
        let loadedData = null;
        let mySvgId = vm.svgId
        axios
            .get(path)
            .then((res) => {
                loadedData = res.data;
                if (loadedData != "default") {
                    loadedData = JSON.parse(loadedData);

                    let columns = Object.keys(loadedData[0]);
                    columns = columns.slice(0, columns.length - 1);
                    loadedData.columns = columns;
                    console.log(loadedData)

                    vm.data = loadedData;

                    vm.draw(loadedData, mySvgId)
                }
            })
            .catch((error) => {
                console.error(error);
            });
    },
};
</script>
  
